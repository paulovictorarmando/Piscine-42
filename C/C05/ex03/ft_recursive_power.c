#include <unistd.h>

int	ft_recursive_power(int nb, int power)
{
	int	res;

	if ((nb == 0 && power != 0) || power < 0)
		return (0);
	if ((power == 0) || ((nb < 0) && (power == 0)))
		return (1);
	else
	{
		res = nb;
		res = nb * ft_recursive_power(res, power - 1);
		return (res);
	}
}
