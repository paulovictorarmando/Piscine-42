#include <unistd.h>

int	ft_iterative_power(int nb, int power)
{
	int	res;

	if ((nb == 0 && power != 0) || power < 0)
		return (0);
	if ((power == 0) || ((nb < 0) && (power == 0)))
		return (1);
	else
	{
		res = 1;
		while (0 < power)
		{
			res = nb * res;
			power--;
		}
		return (res);
	}
}
