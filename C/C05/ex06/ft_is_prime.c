#include <unistd.h>

int	ft_is_prime(int nb)
{
	int	n;
	int	a;

	n = 0;
	if (nb >= 2)
	{
		a = 1;
		while (a < nb)
		{
			if (nb % a == 0)
				n++;
			a++;
		}
		if (!(n > 2))
			return (1);
	}
	return (0);
}
