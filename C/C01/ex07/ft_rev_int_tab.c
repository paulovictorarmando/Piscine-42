#include <unistd.h>

void	ft_rev_int_tab(int *tab, int size)
{
	int	i;
	int	d;
	int	c;

	i = 0;
	d = size -1;
	while (i < d)
	{
		c = tab[d];
		tab[d] = tab[i];
		tab[i] = c;
		i++;
		d--;
	}
}
