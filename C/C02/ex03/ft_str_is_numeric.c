#include <unistd.h>

int	ft_str_is_numeric(char *str)
{
	int	a;
	int	b;

	a = 0;
	if (str[a] == '\0')
	{
		b = 1;
	}
	else
	{
		while (str[a] != '\0')
		{
			if (str[a] >= 48 && str[a] <= 57)
			{
				b = 1;
			}
			else
			{
				b = 0;
				break ;
			}
			a++;
		}
	}
	return (b);
}
